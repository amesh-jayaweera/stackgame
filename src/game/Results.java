    private void fillTable(){
        tableModel.setRowCount(0);
        int turnNo = 1;
        while(queue.front != null){
            tableModel.addRow(new Object[]{turnNo,queue.front.key});
            queue.dequeue();
            turnNo += 1;
        }
    }
    
